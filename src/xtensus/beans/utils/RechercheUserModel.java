package xtensus.beans.utils;

public class RechercheUserModel{
	private String idUser;
	private String nameUser;
	private String surnameUser;
	private String shortnameUser;
	private String adressUser;
	private String mailUser;
	private String telephoneNumberUser;
	private String facsimileTelephoneNumberUser;
	private String codePostalUser;
	
	public RechercheUserModel(){
		this.idUser = "";
		this.nameUser = "";
		this.surnameUser = "";
		this.shortnameUser = "";
		this.adressUser = "";
		this.mailUser = "";
		this.telephoneNumberUser = "";
		this.facsimileTelephoneNumberUser = "";
		this.codePostalUser = "";
	}
	
	public void setNameUser(String nameUser) {
		this.nameUser = nameUser;
	}
	public String getNameUser() {
		return nameUser;
	}
	public void setSurnameUser(String surnameUser) {
		this.surnameUser = surnameUser;
	}
	public String getSurnameUser() {
		return surnameUser;
	}
	public void setAdressUser(String adressUser) {
		this.adressUser = adressUser;
	}
	public String getAdressUser() {
		return adressUser;
	}
	public void setMailUser(String mailUser) {
		this.mailUser = mailUser;
	}
	public String getMailUser() {
		return mailUser;
	}
	public void setShortnameUser(String shortnameUser) {
		this.shortnameUser = shortnameUser;
	}

	public String getShortnameUser() {
		return shortnameUser;
	}

	public void setIdUser(String idUser) {
		this.idUser = idUser;
	}

	public String getIdUser() {
		return idUser;
	}

	public void setTelephoneNumberUser(String telephoneNumberUser) {
		this.telephoneNumberUser = telephoneNumberUser;
	}

	public String getTelephoneNumberUser() {
		return telephoneNumberUser;
	}

	public void setFacsimileTelephoneNumberUser(
			String facsimileTelephoneNumberUser) {
		this.facsimileTelephoneNumberUser = facsimileTelephoneNumberUser;
	}

	public String getFacsimileTelephoneNumberUser() {
		return facsimileTelephoneNumberUser;
	}

	public void setCodePostalUser(String codePostalUser) {
		this.codePostalUser = codePostalUser;
	}

	public String getCodePostalUser() {
		return codePostalUser;
	}
}