package xtensus.beans.utils;

import xtensus.gnl.entity.Notification;
import xtensus.ldap.model.Person;

public class ListeNotificationByUserAndEtat {

	private Notification notification;
	private Person utilisateur;
	private boolean etat;
	
	
	public void setNotification(Notification notification) {
		this.notification = notification;
	}
	public Notification getNotification() {
		return notification;
	}
	public void setUtilisateur(Person utilisateur) {
		this.utilisateur = utilisateur;
	}
	public Person getUtilisateur() {
		return utilisateur;
	}
	public void setEtat(boolean etat) {
		this.etat = etat;
	}
	public boolean isEtat() {
		return etat;
	}


	
}
